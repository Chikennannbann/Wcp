package capsule.human;

public class HumanCapsule02 {
	private String name;
	private int age;
	
	public HumanCapsule02(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return this.age;
	}

}
