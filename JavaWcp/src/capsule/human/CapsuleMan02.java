package capsule.human;

public class CapsuleMan02 {
	private String name;
	private int age;
	
	public CapsuleMan02(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getAge() {
		return this.age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}

}
